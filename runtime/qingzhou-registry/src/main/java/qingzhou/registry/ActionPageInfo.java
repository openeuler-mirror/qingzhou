package qingzhou.registry;

public class ActionPageInfo {
    public String icon;
    public String forwardTo;
    public int shownOnList;
    public int shownOnListHead;

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getForwardTo() {
        return forwardTo;
    }

    public void setForwardTo(String forwardTo) {
        this.forwardTo = forwardTo;
    }

    public int getShownOnList() {
        return shownOnList;
    }

    public void setShownOnList(int shownOnList) {
        this.shownOnList = shownOnList;
    }

    public int getShownOnListHead() {
        return shownOnListHead;
    }

    public void setShownOnListHead(int shownOnListHead) {
        this.shownOnListHead = shownOnListHead;
    }
}
